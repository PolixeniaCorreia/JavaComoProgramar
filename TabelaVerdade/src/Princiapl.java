
public class Princiapl {

	public static void main(String[] args) {
		System.out.println("Condicional AND (&&) \n"+
				"false && false: "+ (false && false)+ "\n"+
				"false && true: " + (false && true)+ "\n"+
				"true && false: " + (true && false)+ "\n"+
				"true && true: " + (true && true)); 
		
		System.out.println("");
		
		System.out.println("Condicional OR (||) \n"+
				"false || false: "+ (false || false)+ "\n"+
				"false || true: " + (false || true)+ "\n"+
				"true || false: " + (true || false)+ "\n"+
				"true || true: " + (true || true));
		
		System.out.println("");
		
		System.out.println("Boolean logical AND (&) \n"+
				"false & false: "+ (false & false)+ "\n"+
				"false & true: " + (false & true)+ "\n"+
				"true & false: " + (true & false)+ "\n"+
				"true & true: " + (true & true));
		
		System.out.println("");
		
		System.out.println("Boolean logical inclusive OR (|) \n"+
				"false | false: "+ (false | false)+ "\n"+
				"false | true: " + (false | true)+ "\n"+
				"true | false: " + (true | false)+ "\n"+
				"true | true: " + (true | true));
		
		System.out.println("");
		
		System.out.println("Boolean logical exclusive OR (^) \n"+
				"false ^ false: "+ (false ^ false)+ "\n"+
				"false ^ true: " + (false ^ true)+ "\n"+
				"true ^ false: " + (true ^ false)+ "\n"+
				"true ^ true: " + (true ^ true));
		
		System.out.println("");
		
		System.out.println("Logical NOT (!) \n"+
				"!false: " + (!false)+ "\n"+
				"!true: " + (!true));
		
	}

}
